package Pemerintahan;

/**
 *
 * @author ACER
 */
public class PemerintahanProvinsi {

    /**
     * @return the WilayahKerja
     */
    protected String getWilayahKerja() {
        return WilayahKerja;
    }

    /**
     * @param WilayahKerja the WilayahKerja to set
     */
    protected void setWilayahKerja(String WilayahKerja) {
        this.WilayahKerja = WilayahKerja;
    }

    /**
     * @return the Pemimpin
     */
    protected String getPemimpin() {
        return Pemimpin;
    }

    /**
     * @param Pemimpin the Pemimpin to set
     */
    protected void setPemimpin(String Pemimpin) {
        this.Pemimpin = Pemimpin;
    }

    /**
     * @return the Fungsi
     */
    protected String getFungsi() {
        return Fungsi;
    }

    /**
     * @param Fungsi the Fungsi to set
     */
    protected void setFungsi(String Fungsi) {
        this.Fungsi = Fungsi;
    }

    /**
     * @param args the command line arguments
     */
       private String WilayahKerja;
       private String Pemimpin;
       private String Fungsi;
    
    
}
